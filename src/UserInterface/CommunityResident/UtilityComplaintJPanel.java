    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
          
       String grievancetype = comboUtilityType.getSelectedItem().toString();
       String priority = comboPrioritybox.getSelectedItem().toString();
       String message = jtxtcomments.getText();
        
       if (grievancetype.isEmpty()||priority.isEmpty()||message.isEmpty())
         {
             
            JOptionPane.showMessageDialog(this, "One or More fields are empty !!", "Empty Fields", 2);
            return;
            
         }

       Complaint complaint = new Complaint();
       complaint.setMessage(message);
       complaint.setSender(userAccount);
       complaint.setPriority(priority);
       complaint.setOrganizationType(grievancetype);
       complaint.setStatus("Complaint raised");
       complaint.setRequestType("Complaint");

        userAccount.getWorkQueue().addWorkAppeal(complaint);
       
       Organization organization = (Organization) comboUtilityType.getSelectedItem();
       
       
       organization.getWorkQueue().addWorkAppeal(complaint);
    
       JOptionPane.showMessageDialog(this, "Request placed successfully !!", "Request", 1);
       comboUtilityType.setSelectedIndex(0);
       comboPrioritybox.setSelectedIndex(0);
       jtxtcomments.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

